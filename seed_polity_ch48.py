def _seed_polity_ch48_notes_inner(conn, db_exec_fn, row_to_dict_fn, use_postgres, force=False):
    ph = '%s' if use_postgres else '?'
    existing = row_to_dict_fn(db_exec_fn(conn,
        f"SELECT id FROM study_notes WHERE topic={ph} AND chapter_num={ph}",
        (_TOPIC, _CH)).fetchone() or {})
    if existing and not force:
        return existing['id']
    import json
    sections_json = json.dumps(_SECTIONS)
    if existing and force:
        db_exec_fn(conn, f"DELETE FROM study_notes WHERE topic={ph} AND chapter_num={ph}", (_TOPIC, _CH))
    db_exec_fn(conn,
        f"INSERT INTO study_notes (chapter_num,subject,topic,chapter_title_te,chapter_title_en,pages_ref,sections_json) "
        f"VALUES ({ph},{ph},{ph},{ph},{ph},{ph},{ph})",
        (_CH, _SUBJECT, _TOPIC, _TITLE_TE, _TITLE_EN, _PAGES, sections_json))
    row = row_to_dict_fn(db_exec_fn(conn,
        f"SELECT id FROM study_notes WHERE topic={ph} AND chapter_num={ph}",
        (_TOPIC, _CH)).fetchone())
    return row['id']


