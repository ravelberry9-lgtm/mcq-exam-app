def _seed_ch10_ancient_mcqs_inner(conn, db_exec_fn, row_to_dict_fn, use_postgres):
    """Seed Chapter 10 (Gupta Empire) MCQs into chapter_mcqs table."""
    ph = "%s" if use_postgres else "?"

    if use_postgres:
        db_exec_fn(conn, '''CREATE TABLE IF NOT EXISTS chapter_mcqs (
            id SERIAL PRIMARY KEY, study_note_id INTEGER NOT NULL,
            section_idx INTEGER NOT NULL DEFAULT 0,
            difficulty INTEGER NOT NULL DEFAULT 1,
            q_te TEXT NOT NULL,
            opt_a TEXT NOT NULL, opt_b TEXT NOT NULL,
            opt_c TEXT NOT NULL, opt_d TEXT NOT NULL,
            correct TEXT NOT NULL, explanation_te TEXT NOT NULL DEFAULT '',
            created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP)''')
    else:
        db_exec_fn(conn, '''CREATE TABLE IF NOT EXISTS chapter_mcqs (
            id INTEGER PRIMARY KEY AUTOINCREMENT, study_note_id INTEGER NOT NULL,
            section_idx INTEGER NOT NULL DEFAULT 0,
            difficulty INTEGER NOT NULL DEFAULT 1,
            q_te TEXT NOT NULL,
            opt_a TEXT NOT NULL, opt_b TEXT NOT NULL,
            opt_c TEXT NOT NULL, opt_d TEXT NOT NULL,
            correct TEXT NOT NULL, explanation_te TEXT NOT NULL DEFAULT "",
            created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP)''')

    if not use_postgres:
        conn.commit()

    cur = db_exec_fn(conn,
        f"SELECT id FROM study_notes WHERE subject={ph} AND topic={ph} AND chapter_num={ph}",
        ('GK', 'Indian_History', 10))
    row = cur.fetchone()
    if not row:
        return {"success": False, "error": "Chapter 10 study note not found. Seed notes first."}

    note_id = list(row)[0]

    # Check if already seeded
    cur2 = db_exec_fn(conn,
        f"SELECT COUNT(*) FROM chapter_mcqs WHERE study_note_id={ph}", (note_id,))
    count_row = cur2.fetchone()
    existing_count = list(count_row)[0] if count_row else 0
    if existing_count > 0:
        return {
            "success": True,
            "message": f"Chapter 10 MCQs already seeded ({existing_count} questions). Use notes re-seed with force=True to reset.",
            "total": existing_count,
        }

    inserted = 0
    for (sec_idx, diff, q, a, b, c, d, correct, expl) in CH10_MCQS:
        db_exec_fn(conn,
            f"""INSERT INTO chapter_mcqs
                (study_note_id, section_idx, difficulty, q_te, opt_a, opt_b, opt_c, opt_d, correct, explanation_te)
                VALUES ({ph},{ph},{ph},{ph},{ph},{ph},{ph},{ph},{ph},{ph})""",
            (note_id, sec_idx, diff, q, a, b, c, d, correct, expl))
        inserted += 1

    if not use_postgres:
        conn.commit()

    easy   = sum(1 for t in CH10_MCQS if t[1] == 1)
    medium = sum(1 for t in CH10_MCQS if t[1] == 2)
    hard   = sum(1 for t in CH10_MCQS if t[1] == 3)

