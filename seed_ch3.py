    """Seed Chapter 3 MCQs."""
    if use_postgres:
        db_exec_fn(conn, '''CREATE TABLE IF NOT EXISTS chapter_mcqs (
            id SERIAL PRIMARY KEY,
            study_note_id INTEGER NOT NULL,
            section_idx INTEGER NOT NULL,
            difficulty INTEGER NOT NULL DEFAULT 1,
            q_te TEXT NOT NULL,
            opt_a TEXT NOT NULL, opt_b TEXT NOT NULL,
            opt_c TEXT NOT NULL, opt_d TEXT NOT NULL,
            correct TEXT NOT NULL,
            explanation_te TEXT NOT NULL DEFAULT '')''')
    else:
        db_exec_fn(conn, '''CREATE TABLE IF NOT EXISTS chapter_mcqs (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            study_note_id INTEGER NOT NULL,
            section_idx INTEGER NOT NULL,
            difficulty INTEGER NOT NULL DEFAULT 1,
            q_te TEXT NOT NULL,
            opt_a TEXT NOT NULL, opt_b TEXT NOT NULL,
            opt_c TEXT NOT NULL, opt_d TEXT NOT NULL,
            correct TEXT NOT NULL,
            explanation_te TEXT NOT NULL DEFAULT "")''')

    cur = db_exec_fn(conn, "SELECT id FROM study_notes WHERE subject='GK' AND topic='Indian_History' AND chapter_num=3")
    row = cur.fetchone()
    if not row:
        return {'success': False, 'error': 'Chapter 3 study note not found. Seed notes first via /api/notes/seed_ch3_ih'}
    note_id = row_to_dict_fn(row)['id']

    placeholder = '%s' if use_postgres else '?'
    db_exec_fn(conn, f"DELETE FROM chapter_mcqs WHERE study_note_id={placeholder}", (note_id,))

    inserted = 0
    for sec_idx, diff, q, a, b, c, d, correct, expl in CH3_MCQS:
        db_exec_fn(conn,
            f"INSERT INTO chapter_mcqs (study_note_id, section_idx, difficulty, q_te, opt_a, opt_b, opt_c, opt_d, correct, explanation_te) VALUES ({placeholder},{placeholder},{placeholder},{placeholder},{placeholder},{placeholder},{placeholder},{placeholder},{placeholder},{placeholder})",
            (note_id, sec_idx, diff, q, a, b, c, d, correct, expl)
        )
        inserted += 1

    conn.commit()
    easy   = sum(1 for m in CH3_MCQS if m[1]==1)
    medium = sum(1 for m in CH3_MCQS if m[1]==2)
    hard   = sum(1 for m in CH3_MCQS if m[1]==3)
    return {
        'success': True,
        'inserted': inserted,
        'easy': easy, 'medium': medium, 'hard': hard,
        'message': f'Chapter 3 MCQs seeded: {inserted} questions ({easy} easy, {medium} medium, {hard} hard).'
    }
