def _seed_polity_ch50_notes_inner(conn, db_exec_fn, row_to_dict_fn, use_postgres, force=False):
    ph = '%s' if use_postgres else '?'
    existing = row_to_dict_fn(db_exec_fn(conn,
        f"SELECT id FROM study_notes WHERE topic={ph} AND chapter_num={ph}",
        (_TOPIC, _CH)).fetchone() or {})
    if existing and not force:
        return existing['id']
    import json
    sections_json = json.dumps(_SECTIONS)
    if existing and force:
        db_exec_fn(conn, f"DELETE FROM study_notes WHERE topic={ph} AND chapter_num={ph}", (_TOPIC, _CH))
    db_exec_fn(conn,
        f"INSERT INTO study_notes (chapter_num,subject,topic,chapter_title_te,chapter_title_en,pages_ref,sections_json) "
        f"VALUES ({ph},{ph},{ph},{ph},{ph},{ph},{ph})",
        (_CH, _SUBJECT, _TOPIC, _TITLE_TE, _TITLE_EN, _PAGES, sections_json))
    row = row_to_dict_fn(db_exec_fn(conn,
        f"SELECT id FROM study_notes WHERE topic={ph} AND chapter_num={ph}",
        (_TOPIC, _CH)).fetchone())
    return row['id']


def _seed_polity_ch50_mcqs_inner(conn, db_exec_fn, row_to_dict_fn, use_postgres):
    ph = '%s' if use_postgres else '?'
    row = row_to_dict_fn(db_exec_fn(conn,
        f"SELECT id FROM study_notes WHERE topic={ph} AND chapter_num={ph}",
        (_TOPIC, _CH)).fetchone())
    note_id = row['id']
    _cnt_row = db_exec_fn(conn,
        f"SELECT COUNT(*) FROM chapter_mcqs WHERE study_note_id={ph}", (note_id,)).fetchone()
    existing = list(row_to_dict_fn(_cnt_row or {}).values())[0] if _cnt_row else 0
    if existing > 0:
        return
    EXAM = 'UPSC'
    _diff_map = {"easy": 1, "medium": 2, "hard": 3}

    for m in _MCQS:
        sec, diff, q, a, b, c, d, ans, expl = m[0], m[1], m[2], m[3], m[4], m[5], m[6], m[7], m[8]
        exam = m[9] if len(m) > 9 else EXAM
        db_exec_fn(conn,
            f"INSERT INTO chapter_mcqs "
            f"(study_note_id,section_idx,difficulty,exam_type,q_te,opt_a,opt_b,opt_c,opt_d,correct,explanation_te) "
            f"VALUES ({ph},{ph},{ph},{ph},{ph},{ph},{ph},{ph},{ph},{ph},{ph})",
            (note_id, sec, _diff_map.get(diff, 1), exam, q, a, b, c, d, ans, expl))


