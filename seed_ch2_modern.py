def _seed_ch2_modern_mcqs_inner(conn, db_exec_fn, row_to_dict_fn, use_postgres):
    """Seed Chapter 2 Modern Indian History MCQs into chapter_mcqs table."""
    ph = "%s" if use_postgres else "?"

    if use_postgres:
        db_exec_fn(conn, '''CREATE TABLE IF NOT EXISTS chapter_mcqs (
            id SERIAL PRIMARY KEY, study_note_id INTEGER NOT NULL,
            section_idx INTEGER NOT NULL DEFAULT 0,
            difficulty INTEGER NOT NULL DEFAULT 1,
            q_te TEXT NOT NULL,
            opt_a TEXT NOT NULL, opt_b TEXT NOT NULL,
            opt_c TEXT NOT NULL, opt_d TEXT NOT NULL,
            correct TEXT NOT NULL, explanation_te TEXT NOT NULL DEFAULT '',
            created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP)''')
    else:
        db_exec_fn(conn, '''CREATE TABLE IF NOT EXISTS chapter_mcqs (
            id INTEGER PRIMARY KEY AUTOINCREMENT, study_note_id INTEGER NOT NULL,
            section_idx INTEGER NOT NULL DEFAULT 0,
            difficulty INTEGER NOT NULL DEFAULT 1,
            q_te TEXT NOT NULL,
            opt_a TEXT NOT NULL, opt_b TEXT NOT NULL,
            opt_c TEXT NOT NULL, opt_d TEXT NOT NULL,
            correct TEXT NOT NULL, explanation_te TEXT NOT NULL DEFAULT "",
            created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP)''')

    if not use_postgres:
        conn.commit()

    # Find the Modern Chapter 2 study note id
    cur = db_exec_fn(conn,
        f"SELECT id FROM study_notes WHERE subject={ph} AND topic={ph} AND chapter_num={ph} AND subtopic={ph}",
        ('GK', 'Indian_History', 2, 'Modern'))
    row = cur.fetchone()
    if not row:
        # Auto-seed notes first
        _seed_ch2_modern_notes_inner(conn, db_exec_fn, row_to_dict_fn, use_postgres, force=False)
        cur = db_exec_fn(conn,
            f"SELECT id FROM study_notes WHERE subject={ph} AND topic={ph} AND chapter_num={ph} AND subtopic={ph}",
            ('GK', 'Indian_History', 2, 'Modern'))
        row = cur.fetchone()

    if not row:
        return {"success": False, "error": "Chapter 2 Modern study note not found. Seed notes first."}

    note_id = list(row)[0]

    # Check if already seeded
    cur2 = db_exec_fn(conn,
        f"SELECT COUNT(*) FROM chapter_mcqs WHERE study_note_id={ph}", (note_id,))
    count_row = cur2.fetchone()
    existing_count = list(count_row)[0] if count_row else 0
    if existing_count > 0:
        return {
            "success": True,
            "message": f"Chapter 2 Modern MCQs already seeded ({existing_count} questions).",
            "total": existing_count,
        }

    inserted = 0
    easy = medium = hard = 0
    for (sec_idx, diff, q, a, b, c, d, correct, expl) in CH2M_MCQS:
        db_exec_fn(conn,
            f"""INSERT INTO chapter_mcqs
                (study_note_id, section_idx, difficulty, q_te, opt_a, opt_b, opt_c, opt_d, correct, explanation_te)
                VALUES ({ph},{ph},{ph},{ph},{ph},{ph},{ph},{ph},{ph},{ph})""",
            (note_id, sec_idx, diff, q, a, b, c, d, correct, expl))
        inserted += 1
        if diff == 1: easy += 1
        elif diff == 2: medium += 1
        else: hard += 1

    if not use_postgres:
        conn.commit()

    return {
        "success": True,
        "message": f"Chapter 2 Modern MCQs seeded! ({inserted} questions)",
        "total": inserted,
        "easy": easy,
        "medium": medium,
        "hard": hard,
    }


