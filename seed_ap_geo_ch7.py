def _seed_ap_geo_ch7_mcqs_inner(conn, db_exec, row_to_dict, USE_POSTGRES):
    ph = '%s' if USE_POSTGRES else '?'
    cur = db_exec(conn, f"SELECT id FROM study_notes WHERE chapter_num={ph} AND topic={ph}", (7, 'AP_Geography'))
    row = cur.fetchone()

    if not row:
        _seed_ap_geo_ch7_notes_inner(conn, db_exec, row_to_dict, USE_POSTGRES, force=False)
        cur = db_exec(conn, f"SELECT id FROM study_notes WHERE chapter_num={ph} AND topic={ph}", (7, 'AP_Geography'))
        row = cur.fetchone()

    note_id = row_to_dict(row)['id']

    db_exec(conn, f"DELETE FROM chapter_mcqs WHERE study_note_id={ph}", (note_id,))

    insert_sql = (
        f"INSERT INTO chapter_mcqs "
        f"(study_note_id, section_idx, difficulty, q_te, opt_a, opt_b, opt_c, opt_d, correct, explanation_te) "
        f"VALUES ({ph},{ph},{ph},{ph},{ph},{ph},{ph},{ph},{ph},{ph})")

    for (sec_idx, diff, q_te, a, b, c, d, correct, expl) in MCQ_DATA:
        db_exec(conn, insert_sql, (note_id, sec_idx, diff, q_te, a, b, c, d, str(correct).lower(), expl))

    if USE_POSTGRES:
        conn.commit()

    return {'success': True, 'inserted': len(MCQ_DATA)}
