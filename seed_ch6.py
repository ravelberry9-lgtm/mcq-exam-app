def _seed_ch6_mcqs_inner(conn, db_exec, row_to_dict, USE_POSTGRES):
    """Insert Chapter 6 MCQs into chapter_mcqs table."""
    ph = '%s' if USE_POSTGRES else '?'

    # Ensure table exists
    try:
        if USE_POSTGRES:
            conn.execute('''CREATE TABLE IF NOT EXISTS chapter_mcqs (
                id SERIAL PRIMARY KEY,
                study_note_id INTEGER NOT NULL,
                section_idx INTEGER NOT NULL DEFAULT 0,
                difficulty INTEGER NOT NULL DEFAULT 1,
                q_te TEXT NOT NULL,
                opt_a TEXT NOT NULL,
                opt_b TEXT NOT NULL,
                opt_c TEXT NOT NULL,
                opt_d TEXT NOT NULL,
                correct TEXT NOT NULL,
                explanation_te TEXT NOT NULL DEFAULT '',
                created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
            )''')
        else:
            conn.execute('''CREATE TABLE IF NOT EXISTS chapter_mcqs (
                id INTEGER PRIMARY KEY AUTOINCREMENT,
                study_note_id INTEGER NOT NULL,
                section_idx INTEGER NOT NULL DEFAULT 0,
                difficulty INTEGER NOT NULL DEFAULT 1,
                q_te TEXT NOT NULL,
                opt_a TEXT NOT NULL,
                opt_b TEXT NOT NULL,
                opt_c TEXT NOT NULL,
                opt_d TEXT NOT NULL,
                correct TEXT NOT NULL,
                explanation_te TEXT NOT NULL DEFAULT '',
                created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
            )''')
        conn.commit()
    except Exception:
        pass

    # Get study_note_id for ch6
    cur = db_exec(conn, f'SELECT id FROM study_notes WHERE chapter_num={ph} AND subject={ph}', (6, 'GK'))
    row = cur.fetchone()
    if not row:
        # Diagnostic: show what IS in study_notes
        try:
            all_cur = db_exec(conn, 'SELECT id, subject, chapter_num FROM study_notes', ())
            all_rows = all_cur.fetchall()
            debug = [dict(r) if hasattr(r, 'keys') else {'id': r[0], 'subject': r[1], 'chapter_num': r[2]} for r in all_rows]
        except Exception as de:
            debug = str(de)
        return {'success': False, 'error': f'Chapter 6 notes not found. DB has: {debug}'}

    note_id = row_to_dict(row)['id'] if hasattr(row, 'keys') else row[0]

    # Delete existing ch6 MCQs
    db_exec(conn, f'DELETE FROM chapter_mcqs WHERE study_note_id={ph}', (note_id,))
    if USE_POSTGRES:
        conn.commit()

    # Insert all MCQs
    for (sec_idx, diff, q_te, opt_a, opt_b, opt_c, opt_d, correct, expl) in CH6_MCQS:
        db_exec(conn,
            f'''INSERT INTO chapter_mcqs
                (study_note_id, section_idx, difficulty, q_te,
                 opt_a, opt_b, opt_c, opt_d, correct, explanation_te)
                VALUES ({ph},{ph},{ph},{ph},{ph},{ph},{ph},{ph},{ph},{ph})''',
            (note_id, sec_idx, diff, q_te, opt_a, opt_b, opt_c, opt_d, correct, expl)
        )
    if USE_POSTGRES:
        conn.commit()

    easy   = sum(1 for m in CH6_MCQS if m[1] == 1)
    medium = sum(1 for m in CH6_MCQS if m[1] == 2)
    hard   = sum(1 for m in CH6_MCQS if m[1] == 3)

    return {
        'success': True,
        'message': f'Chapter 6 MCQs seeded: {len(CH6_MCQS)} total.',
        'easy': easy,
        'medium': medium,
        'hard': hard
    }
